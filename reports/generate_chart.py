"""Deterministic static HTML chart generation for round artifacts."""

from __future__ import annotations

import csv
import html
import json
from pathlib import Path


CHART_SCHEMA_VERSION = "round_chart_v1"
SERIES_COLORS = {
    "train": "#2563eb",
    "validation": "#059669",
    "holdout": "#dc2626",
}


def generate_round_chart(
    *,
    output_path: Path,
    run_id: str,
    round_id: str,
    metrics_paths: dict[str, Path],
    trades_paths: dict[str, Path],
) -> Path:
    """Write a deterministic, dependency-free HTML chart for one round."""
    metrics = {
        split: load_metrics(path)
        for split, path in sorted(metrics_paths.items())
    }
    series = {
        split: cumulative_pnl(load_trade_pnls(path))
        for split, path in sorted(trades_paths.items())
    }
    output_path.write_text(
        render_chart_html(
            run_id=run_id,
            round_id=round_id,
            metrics=metrics,
            series=series,
        ),
        encoding="utf-8",
    )
    return output_path


def load_metrics(path: Path) -> dict[str, float | int]:
    """Load a metrics JSON object from disk."""
    payload = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(payload, dict):
        return {}
    return {
        str(key): value
        for key, value in payload.items()
        if isinstance(value, int | float)
    }


def load_trade_pnls(path: Path) -> list[float]:
    """Load trade PnL values from a CSV file."""
    if not path.exists():
        return []
    with path.open("r", encoding="utf-8", newline="") as handle:
        reader = csv.DictReader(handle)
        return [
            float(row.get("pnl", 0.0) or 0.0)
            for row in reader
        ]


def cumulative_pnl(pnls: list[float]) -> list[float]:
    """Return cumulative PnL points including the zero origin."""
    total = 0.0
    points = [0.0]
    for pnl in pnls:
        total += pnl
        points.append(round(total, 6))
    return points


def render_chart_html(
    *,
    run_id: str,
    round_id: str,
    metrics: dict[str, dict[str, float | int]],
    series: dict[str, list[float]],
) -> str:
    """Return deterministic HTML for the round chart artifact."""
    return "\n".join(
        [
            "<!doctype html>",
            '<html lang="en">',
            "<head>",
            '<meta charset="utf-8">',
            '<meta name="viewport" content="width=device-width, initial-scale=1">',
            f'<meta name="suan-chart-schema" content="{CHART_SCHEMA_VERSION}">',
            f"<title>SuanAgent Chart - {escape(run_id)} {escape(round_id)}</title>",
            "<style>",
            chart_css(),
            "</style>",
            "</head>",
            "<body>",
            "<main>",
            "<header>",
            "<h1>SuanAgent Round Chart</h1>",
            f"<p>Run <strong>{escape(run_id)}</strong> / Round <strong>{escape(round_id)}</strong></p>",
            "</header>",
            '<section class="panel">',
            "<h2>Cumulative PnL</h2>",
            render_svg(series),
            '<p class="note">Static HTML generated by deterministic local code. No external assets, scripts, or network calls.</p>',
            "</section>",
            '<section class="panel">',
            "<h2>Metrics</h2>",
            render_metrics_table(metrics),
            "</section>",
            "</main>",
            "</body>",
            "</html>",
            "",
        ]
    )


