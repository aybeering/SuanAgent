def render_svg(series: dict[str, list[float]]) -> str:
    """Return an inline SVG line chart for cumulative PnL series."""
    width = 900
    height = 360
    left = 54
    right = 24
    top = 24
    bottom = 46
    plot_width = width - left - right
    plot_height = height - top - bottom
    values = [
        value
        for points in series.values()
        for value in points
    ] or [0.0]
    min_y = min(0.0, min(values))
    max_y = max(0.0, max(values))
    if min_y == max_y:
        max_y += 1.0
        min_y -= 1.0
    max_x = max((len(points) - 1 for points in series.values()), default=1)
    max_x = max(max_x, 1)
    zero_y = scale_y(0.0, min_y, max_y, top, plot_height)
    lines = [
        f'<svg class="chart" viewBox="0 0 {width} {height}" role="img" aria-label="Cumulative PnL by split">',
        f'<line class="axis" x1="{left}" y1="{top}" x2="{left}" y2="{top + plot_height}" />',
        f'<line class="axis" x1="{left}" y1="{zero_y:.2f}" x2="{left + plot_width}" y2="{zero_y:.2f}" />',
    ]
    for index in range(5):
        y = top + index * plot_height / 4
        value = max_y - index * (max_y - min_y) / 4
        lines.append(
            f'<line class="grid" x1="{left}" y1="{y:.2f}" x2="{left + plot_width}" y2="{y:.2f}" />'
        )
        lines.append(
            f'<text x="10" y="{y + 4:.2f}" font-size="12" fill="#4b5563">{value:.2f}</text>'
        )
    legend_y = height - 20
    for legend_index, (split, points) in enumerate(sorted(series.items())):
        color = SERIES_COLORS.get(split, "#111827")
        path = svg_path(points, min_y, max_y, max_x, left, top, plot_width, plot_height)
        lines.append(
            f'<path class="series" d="{path}" stroke="{color}" />'
        )
        legend_x = left + legend_index * 150
        lines.append('<g class="legend">')
        lines.append(
            f'<line x1="{legend_x}" y1="{legend_y}" x2="{legend_x + 22}" y2="{legend_y}" stroke="{color}" stroke-width="3" />'
        )
        lines.append(
            f'<text x="{legend_x + 30}" y="{legend_y + 4}">{escape(split)}</text>'
        )
        lines.append("</g>")
    lines.append("</svg>")
    return "\n".join(lines)


def svg_path(
    points: list[float],
    min_y: float,
    max_y: float,
    max_x: int,
    left: int,
    top: int,
    plot_width: int,
    plot_height: int,
) -> str:
    """Return an SVG path string for one cumulative PnL series."""
    if not points:
        points = [0.0]
    commands = []
    for index, value in enumerate(points):
        x = left + (index / max_x) * plot_width
        y = scale_y(value, min_y, max_y, top, plot_height)
        command = "M" if index == 0 else "L"
        commands.append(f"{command}{x:.2f},{y:.2f}")
    return " ".join(commands)


def scale_y(
    value: float,
    min_y: float,
    max_y: float,
    top: int,
    plot_height: int,
) -> float:
    """Scale a data value to SVG y coordinates."""
    return top + (max_y - value) / (max_y - min_y) * plot_height


def render_metrics_table(metrics: dict[str, dict[str, float | int]]) -> str:
    """Return an HTML table of selected metrics by split."""
    metric_keys = (
        "ev",
        "total_pnl",
        "max_drawdown",
        "trade_count",
        "fill_rate",
        "avg_slippage",
    )
    lines = [
        "<table>",
        "<thead>",
        "<tr><th>Split</th>"
        + "".join(f"<th>{escape(key)}</th>" for key in metric_keys)
        + "</tr>",
        "</thead>",
        "<tbody>",
    ]
    for split, values in sorted(metrics.items()):
        cells = [f"<td>{escape(split)}</td>"]
        for key in metric_keys:
            value = values.get(key, "")
            cells.append(f"<td>{format_metric(value)}</td>")
        lines.append("<tr>" + "".join(cells) + "</tr>")
    lines.extend(["</tbody>", "</table>"])
    return "\n".join(lines)


def format_metric(value: object) -> str:
    """Return stable metric text for HTML tables."""
    if isinstance(value, int):
        return str(value)
    if isinstance(value, float):
        return f"{value:.6f}"
    return ""


def escape(value: object) -> str:
    """HTML-escape a value."""
    return html.escape(str(value), quote=True)
