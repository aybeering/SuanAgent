"""Deterministic static HTML trade-timeline generation."""

from __future__ import annotations

import csv
import html
from pathlib import Path
from typing import Any


TIMELINE_SCHEMA_VERSION = "trade_timeline_v1"
TIMELINE_COLUMNS = (
    "split",
    "timestamp",
    "market_id",
    "side",
    "requested_price",
    "fill_price",
    "stake",
    "quantity",
    "outcome",
    "pnl",
    "slippage",
    "reason",
)


def generate_trade_timeline(
    *,
    output_path: Path,
    run_id: str,
    round_id: str,
    trades_paths: dict[str, Path],
) -> Path:
    """Write a deterministic, dependency-free trade timeline for one round."""
    rows = timeline_rows(trades_paths)
    output_path.write_text(
        render_timeline_html(
            run_id=run_id,
            round_id=round_id,
            rows=rows,
        ),
        encoding="utf-8",
    )
    return output_path


def timeline_rows(trades_paths: dict[str, Path]) -> list[dict[str, str]]:
    """Return stable timeline rows from split-specific trade CSV files."""
    rows: list[dict[str, str]] = []
    for split, path in sorted(trades_paths.items()):
        if not path.exists():
            continue
        with path.open("r", encoding="utf-8", newline="") as handle:
            reader = csv.DictReader(handle)
            for row in reader:
                rows.append(
                    {
                        column: str(row.get(column, ""))
                        for column in TIMELINE_COLUMNS
                        if column != "split"
                    }
                    | {"split": split}
                )
    return sorted(
        rows,
        key=lambda row: (
            row.get("timestamp", ""),
            row.get("split", ""),
            row.get("market_id", ""),
        ),
    )


def render_timeline_html(
    *,
    run_id: str,
    round_id: str,
    rows: list[dict[str, str]],
) -> str:
    """Return deterministic HTML for the trade timeline artifact."""
    summary = timeline_summary(rows)
    return "\n".join(
        [
            "<!doctype html>",
            '<html lang="en">',
            "<head>",
            '<meta charset="utf-8">',
            '<meta name="viewport" content="width=device-width, initial-scale=1">',
            f'<meta name="suan-timeline-schema" content="{TIMELINE_SCHEMA_VERSION}">',
            f"<title>SuanAgent Trade Timeline - {escape(run_id)} {escape(round_id)}</title>",
            "<style>",
            timeline_css(),
            "</style>",
            "</head>",
            "<body>",
            "<main>",
            "<header>",
            "<h1>SuanAgent Trade Timeline</h1>",
            f"<p>Run <strong>{escape(run_id)}</strong> / Round <strong>{escape(round_id)}</strong></p>",
            "</header>",
            render_summary(summary),
            '<section class="panel">',
            "<h2>Trades</h2>",
            render_trade_table(rows),
            '<p class="note">Static HTML generated by deterministic local code. No external assets, scripts, or network calls.</p>',
            "</section>",
            "</main>",
            "</body>",
            "</html>",
            "",
        ]
    )


