def timeline_summary(rows: list[dict[str, str]]) -> dict[str, Any]:
    """Return simple deterministic summary values for timeline rendering."""
    pnls = [float_or_zero(row.get("pnl", "")) for row in rows]
    slippages = [float_or_zero(row.get("slippage", "")) for row in rows]
    return {
        "trade_count": len(rows),
        "positive_trades": sum(1 for pnl in pnls if pnl > 0.0),
        "negative_trades": sum(1 for pnl in pnls if pnl < 0.0),
        "total_pnl": round(sum(pnls), 6),
        "avg_slippage": round(sum(slippages) / len(slippages), 6) if slippages else 0.0,
    }


def render_summary(summary: dict[str, Any]) -> str:
    """Return summary tiles for the trade timeline."""
    tiles = (
        ("Trade count", summary["trade_count"]),
        ("Positive", summary["positive_trades"]),
        ("Negative", summary["negative_trades"]),
        ("Total PnL", f"{summary['total_pnl']:.6f}"),
        ("Avg slippage", f"{summary['avg_slippage']:.6f}"),
    )
    return "\n".join(
        [
            '<section class="summary">',
            *[
                f'<div class="tile"><span>{escape(label)}</span><strong>{escape(value)}</strong></div>'
                for label, value in tiles
            ],
            "</section>",
        ]
    )


def render_trade_table(rows: list[dict[str, str]]) -> str:
    """Return the timeline table."""
    lines = [
        "<table>",
        "<thead>",
        "<tr>" + "".join(f"<th>{escape(column)}</th>" for column in TIMELINE_COLUMNS) + "</tr>",
        "</thead>",
        "<tbody>",
    ]
    for row in rows:
        pnl = float_or_zero(row.get("pnl", ""))
        row_class = "pnl-positive" if pnl > 0 else "pnl-negative" if pnl < 0 else "pnl-flat"
        cells = []
        for column in TIMELINE_COLUMNS:
            css_class = ' class="pnl"' if column == "pnl" else ""
            cells.append(f"<td{css_class}>{escape(row.get(column, ''))}</td>")
        lines.append(f'<tr class="{row_class}">' + "".join(cells) + "</tr>")
    lines.extend(["</tbody>", "</table>"])
    return "\n".join(lines)


def float_or_zero(value: str) -> float:
    """Return a float value or zero when parsing fails."""
    try:
        return float(value)
    except ValueError:
        return 0.0


def escape(value: object) -> str:
    """HTML-escape a value."""
    return html.escape(str(value), quote=True)
